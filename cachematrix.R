##Caching the Inverse(using "solve()") of a Matrix

##In this example we use the `<<-` operator which can be used to
##assign a value to an object in an environment that is different from the
##current environment. Below are two functions that are used to create a
##special object that stores a numeric matrix  and caches its inverse.
##
##The first function, `makeMatrix` creates a special "matrix", which is
##really a list containing a function to
##
##1.  set the value of the matrix
##2.  get the value of the matrix
##3.  set the value of the inverse("solve()")
##4.  get the value of the inverse






makeCacheMatrix <- function(x = matrix()) {
            m <- NULL
            set <- function(y) {
                    x <<- y
                    m <<- NULL
            }
            get <- function() x
            setsolve <- function(solve) m <<- solve
            getsolve <- function() m
            list(set = set, get = get,
                 setsolve = setsolve,
                 getsolve = getsolve)
    }






##The following function calculates the inverse of the special "matrix"
##created with the above function. However, it first checks to see if the
##inverse has already been calculated. If so, it `get`s the inverse from the
##cache and skips the computation. Otherwise, it calculates the inverse of
##the data and sets the value of the inverse in the cache via the `setsolve`
##function.
        
## Return a matrix that is the inverse of 'x'
